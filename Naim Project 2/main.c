#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{   int x = 10;
    int y = 3;
    float a = 5.5;
    float b = 8.7;

    printf("Addition of a and b : %f\n", a + b);
    printf("Subtraction of a and b : %f\n", a - b);
    printf("Multiplication of x and y : %d\n", x * y);
    printf("Division of x and a : %f\n", x / a);
    printf("Modulus of x and y : %d\n", x % y);
    return 0;
}
